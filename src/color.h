#ifndef COLOR_H
#define COLOR_H

// The Color enum represents different colors that can be used in the game
// to customize the appearance of various elements on the console.
//
// RESET: Resets the color to the default color.
// GREEN: Represents the color green.
// RED: Represents the color red.
// BLUE: Represents the color blue.
// YELLOW: Represents the color yellow.
// LIME: Represents the color lime green.
enum class Color { RESET, GREEN, RED, BLUE, YELLOW, LIME };

#endif  // COLOR_H